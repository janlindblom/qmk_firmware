/**
 * @brief Deferred function to calculate the next animation frame.
 *
 * @param trigger_time when to trigger the function.
 * @param cb_arg any arguments to pass to the function.
 * @return uint32_t the next trigger time.
 */
uint32_t pet_animation_phases(uint32_t trigger_time, void *cb_arg) {
    static uint16_t anim_frame_duration = 500;
#        ifdef WPM_ENABLE
    if (current_wpm <= OLED_SLEEP_SPEED) {
        animation_frame     = (animation_frame + 1) % OLED_SLEEP_FRAMES;
        animation_type      = 0;
        anim_frame_duration = 500;
    } else if (current_wpm > OLED_WAKE_SPEED && current_wpm < OLED_KAKI_SPEED) {
        animation_frame     = (animation_frame + 1) % OLED_WAKE_FRAMES;
        animation_type      = 1;
        anim_frame_duration = 800;
    } else if (current_wpm >= OLED_KAKI_SPEED) {
        animation_frame     = (animation_frame + 1) % OLED_KAKI_FRAMES;
        animation_type      = 2;
        anim_frame_duration = 500;
    }
#        endif // WPM_ENABLE
    return (uint32_t)anim_frame_duration;
}

