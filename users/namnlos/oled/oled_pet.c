/**
 * @brief Determine where to place the cursor on the OLED display.
 * @param  offset: The offset from the top of the display.
 * @return The line where to place the cursor based on whether or not we are leaving space for a pet.
 */
uint8_t PET_OFFSET(uint8_t offset) {
#    if defined(RENDER_PET) && defined(OLED_VERTICAL)
#        if OLED_COUNT == 2
#            ifdef RENDER_KEYBOARD_LOGO
    return (is_keyboard_master()) ? 5 + offset + PET_LINE : offset;
#            else
    return (((render_pet_user() || render_icon_user()) && !is_keyboard_master()) ? 5 + offset + PET_LINE : offset);
#            endif
#        else
    return ((render_pet_user() || render_icon_user()) ? 5 + offset + PET_LINE : offset);
#        endif
#    else
    return offset;
#    endif
}

/**
 * @brief Keymap level function to ask whether the PET should be rendered.
 *
 * Weak function to be overridden by keymap.c.
 *
 */
__attribute__((weak)) bool render_pet_keymap(void) {
    return true;
}

/**
 * @brief Userspace function to ask whether the pet should be rendered.
 *
 * @return true if it should,
 * @return false otherwise.
 */
bool render_pet_user(void) {
    return render_pet_keymap();
}

