#        ifdef DEFERRED_EXEC_ENABLE
deferred_token pet_token = INVALID_DEFERRED_TOKEN;

void pet_control_on(uint32_t delay) {
    if (pet_token != INVALID_DEFERRED_TOKEN) {
        cancel_deferred_exec(pet_token);
    }
    pet_token = defer_exec(delay, pet_animation_phases, NULL);
}

void pet_control_off(void) {
    if (pet_token != INVALID_DEFERRED_TOKEN) {
        cancel_deferred_exec(pet_token);
        pet_token = INVALID_DEFERRED_TOKEN;
    }
}
#        endif // DEFERRED_EXEC_ENABLE

